/*
------------------------------------------
题目：将华氏温度64F转化为摄氏度
公式：c=5/9(f-32) 
------------------------------------------
*/
#include "stdio.h" 

int main (void){
	float f, c;
	f = 64.0;
	c = (5.0 / 9) * (f - 32);
	printf ("f = %f\nc = %f\n", f, c);
	
	return 0;
}
