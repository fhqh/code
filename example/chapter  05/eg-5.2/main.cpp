/*
-------------------------------------------------------------------------
题目：用do...while...语句求1+2+3+...+100的和。 
-------------------------------------------------------------------------
*/
#include "stdio.h"

int main (void){
	int i = 1, sum = 0;
	do {
		sum = sum + i;
		i++;
	}
	while (i <= 100);
	printf ("sum = %d\n", sum);
	
	return 0;
} 
